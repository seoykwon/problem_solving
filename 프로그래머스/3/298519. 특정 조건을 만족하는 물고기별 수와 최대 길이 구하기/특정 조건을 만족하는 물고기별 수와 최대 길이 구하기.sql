SELECT COUNT(*) FISH_COUNT, MAX(LENGTH) MAX_LENGTH, FISH_TYPE
FROM FISH_INFO
GROUP BY FISH_TYPE
HAVING AVG(IF(IFNULL(LENGTH, 0) <= 10, 10, LENGTH)) >= 33
ORDER BY FISH_TYPE